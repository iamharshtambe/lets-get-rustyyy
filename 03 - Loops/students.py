# lists
students1 = ["Harsh", "Shreekant", "Rushikesh", "Sejal", "Pooja"]

for i in range(len(students1)):
    print(i + 1, students1[i])

# dictionaries
students2 = {
    "Harsh": "Tambe",
    "Shreekant": "Belwalkar",
    "Rushikesh": "Patil",
    "Sejal": "Patil",
    "Pooja": "Narkar",
}

for student in students2:
    print(student, students2[student])
